package Value;

public class SkillListValue {
	private String num;
	private String shikakuid;
	private String shikakumei;
	private String seiseki;
	private String sakujosumi;
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getShikakuid() {
		return shikakuid;
	}
	public void setShikakuid(String shikakuid) {
		this.shikakuid = shikakuid;
	}
	public String getShikakumei() {
		return shikakumei;
	}
	public void setShikakumei(String shikakumei) {
		this.shikakumei = shikakumei;
	}
	public String getSeiseki() {
		return seiseki;
	}
	public void setSeiseki(String seiseki) {
		this.seiseki = seiseki;
	}
	public String getSakujosumi() {
		return sakujosumi;
	}
	public void setSakujosumi(String sakujosumi) {
		this.sakujosumi = sakujosumi;
	}
	

}
