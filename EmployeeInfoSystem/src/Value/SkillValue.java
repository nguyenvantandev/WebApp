package Value;

public class SkillValue {

	private String num;
	private String shikakuid;
	private String namae;
	private String shutokuday;
	private String seiseki;
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}

	public String getShikakuid() {
		return shikakuid;
	}
	public void setShikakuid(String shikakuid) {
		this.shikakuid = shikakuid;
	}
	public String getNamae() {
		return namae;
	}
	public void setNamae(String namae) {
		this.namae = namae;
	}
	public String getShutokuday() {
		return shutokuday;
	}
	public void setShutokuday(String shutokuday) {
		this.shutokuday = shutokuday;
	}
	public String getSeiseki() {
		return seiseki;
	}
	public void setSeiseki(String seiseki) {
		this.seiseki = seiseki;
	}
}
